package javatask;

public class up_casting {
	
	public static void main(String[] args) {
		 // narrowing
		System.out.println("=====narrowing====");
		double p=100;
	    int i=(int)p;
	    System.out.println(i);

	// wideing
	System.out.println("=====wideing===");
	 byte b=110;
	 int k=b;
	System.out.println(k);
	
	
	}
	
	
	


}
