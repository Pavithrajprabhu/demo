package javatask;

public class nested_dot {
 public static void main(String[] args) {
	
	 for (int i = 0; i <=5; i++) 
	 {
	System.out.println();
		for (int j = 0; j <=i; j++)
		{
			System.out.print(j);
		}
	}
	 for (int g = 5; g >=0; g--)
	 {
		System.out.println();
		for (int h = g; h >=0; h--) 
		{
			System.out.print(h);
		}
	}
	 for (int i = 0; i <=5; i++) 
	 {
	System.out.println();
		for (int j = 0; j <=i; j++)
		{
			System.out.print("*");
		}
	}
	 for (int g = 5; g >=0; g--)
	 {
		System.out.println();
		for (int h = g; h >=0; h--) 
		{
			System.out.print("*");
		}
	}
	 
}
	
	
	
	
	
}
