package javatask;

public class loop_method {
	// for loop
	
	public static void main(String[] args) {
		for (int i = 0; i<=2; i++) {
			System.out.println(i);
		}
		

	// while loop
		
		
int c=1;
while (c<=6) {
	System.out.println(c);
	c++;
}


 //do while loop
int d=0;
do {
	System.out.println(d);
	d++;
}  while (d<=9);

}

	
	
}
